function createAniProjectPage(projCollection) {
	projectTitleElem.innerText = projCollection["itemTitle"] + " Animation";
	projectDescElem.innerText = projCollection["description"];

	for (let i = 0; i < projCollection["animation"].length; i++) {
		let newProjDiv = document.createElement("DIV");
		newProjDiv.setAttribute("id","video-box");
		newProjDiv.style.backgroundColor = projCollection["backgroundColor"];
		let newProjVid = document.createElement("VIDEO");
		let newProjSource = document.createElement("SOURCE");
		newProjSource.src = projCollection["animation"][i];
		newProjSource.type = "video/mp4";
		newProjVid.controls = true;
		newProjVid.autoplay = true;
		newProjVid.loop = true;
		newProjVid.muted = true;
		newProjVid.appendChild(newProjSource);
		newProjDiv.appendChild(newProjVid);

		if ((projCollection["animation"].length % 3) == 1 && i == (projCollection["animation"].length - 1)) {
			newProjDiv.style.gridColumnStart = 2;
		}

		displayGridElem.appendChild(newProjDiv);
	}
}

function createRenProjectPage(projCollection) {
	projectTitleElem.innerText = projCollection["itemTitle"] + " Renders";
	projectDescElem.innerText = projCollection["description"];

	for (let i = 0; i < projCollection["imageArray"].length; i++) {
		let newProjDiv = document.createElement("DIV");
		newProjDiv.setAttribute("id","image-box");
		newProjDiv.style.backgroundColor = projCollection["backgroundColor"];
		let newProjImg = document.createElement("IMG");
		newProjImg.src = projCollection["imageArray"][i];
		newProjDiv.appendChild(newProjImg);

		let newProjTitleDiv = document.createElement("DIV");
		newProjTitleDiv.setAttribute("id","image-descript");
		newProjTitleDiv.style.backgroundColor = projCollection["backgroundColor"];
		let newProjTitleText = document.createElement("p");
		newProjTitleText.setAttribute("id","img-descript");
		if (projCollection["textColor"] != "") { newProjTitleText.style.color = projCollection["textColor"]; }
		newProjTitleText.innerText = projCollection["descriptArray"][i];
		newProjTitleDiv.appendChild(newProjTitleText);
		newProjDiv.appendChild(newProjTitleDiv);

		if ((projCollection["imageArray"].length % 3) == 1 && i == (projCollection["imageArray"].length - 1)) {
			newProjDiv.style.gridColumnStart = 2;
		}

		displayGridElem.appendChild(newProjDiv);
	}
}