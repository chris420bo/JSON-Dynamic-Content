let projectTitleElem;
let projectDescElem;
let displayGridElem;
let projectDispElem; 

document.addEventListener("DOMContentLoaded", function() {
	projectTitleElem = document.getElementById("projectTitle");
	projectDescElem = document.getElementById("projectDescript");
	displayGridElem = document.getElementById("displayGrid");
	projectDispElem = document.getElementById("projectDisplay");

	let queryString = window.location.search;
	let urlParams = new URLSearchParams(queryString);
	let urlSection = urlParams.get('section');
	let urlID = urlParams.get('id');

	if (urlSection != "item") {
		if (urlSection == "animations") {
		  	projectTitleElem.innerText = "Project Animations:";
		}
		else if (urlSection == "renders") {
		  	projectTitleElem.innerText = "Project Renders:";
		}

		/* Create thumbnails for matching category, or all */
		for (let i = 0; i < portfolioCollection.length; i++) {
			if (urlSection == "" || urlSection == null){
				if (portfolioCollection[i]["animation"].length >= 1) {
					createAnimationPreview(portfolioCollection[i]);
				}
				createRenderPreview(portfolioCollection[i]);
			}
			else if (urlSection == "animations") {
				createAnimationPreview(portfolioCollection[i]);
			}
			else if (urlSection == "renders") {
				createRenderPreview(portfolioCollection[i]);
			}
		}
	}

	else {
		for (let i = 0; i < portfolioCollection.length; i++) {
			if ((portfolioCollection[i]["id"]+"Animation") == urlID) {
				createAniProjectPage(portfolioCollection[i]);
			}
			else if ((portfolioCollection[i]["id"]+"Render") == urlID) {
				createRenProjectPage(portfolioCollection[i]);
			}
		}
	}
});

function createAnimationPreview(projCollection) {
	let newPreviewLink = document.createElement("A");
	newPreviewLink.href = "index.html?section=item&id=" + projCollection["id"] + "Animation";

	let newPreviewDiv = document.createElement("DIV");
	newPreviewDiv.setAttribute("id","video-box");
	newPreviewDiv.style.backgroundColor = projCollection["backgroundColor"];
	newPreviewLink.appendChild(newPreviewDiv);

	let newPreviewVid = document.createElement("VIDEO");
	let newPreviewSource = document.createElement("SOURCE");
	newPreviewSource.src = projCollection["animation"][0];
	newPreviewSource.type = "video/mp4";
	newPreviewVid.autoplay = true;
	newPreviewVid.loop = true;
	newPreviewVid.muted = true;
	newPreviewVid.appendChild(newPreviewSource);
	newPreviewDiv.appendChild(newPreviewVid);

	let newPreTitleDiv = document.createElement("DIV");
	newPreTitleDiv.setAttribute("id","image-descript");
	newPreTitleDiv.style.backgroundColor = projCollection["backgroundColor"];
	let newPreTitleText = document.createElement("p");
	newPreTitleText.setAttribute("id","img-descript");
	if (projCollection["textColor"] != "") { newPreTitleText.style.color = projCollection["textColor"]; }
	newPreTitleText.innerText = projCollection["itemTitle"] + " Animation";
	newPreTitleDiv.appendChild(newPreTitleText);
	newPreviewDiv.appendChild(newPreTitleDiv);

	displayGridElem.appendChild(newPreviewLink);
}

